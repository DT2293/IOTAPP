router.post("/register", async (req, res) => {
    try {
        let { username, email, phonenumber, password } = req.body;

        email = email.toLowerCase().trim();
        username = username.toLowerCase().trim();
        phonenumber = phonenumber.trim();  // üîπ S·ª≠a phonenumber kh√¥ng c·∫ßn chuy·ªÉn th√†nh lowercase

        const existingUser = await User.findOne({ $or: [{ email }, { username }, { phonenumber }] }); // üîπ Ki·ªÉm tra phonenumber ƒë√£ t·ªìn t·∫°i
        if (existingUser) {
            return res.status(400).json({
                error: existingUser.email === email ? "Email ƒë√£ ƒë∆∞·ª£c s·ª≠ d·ª•ng!" : 
                       existingUser.username === username ? "Username ƒë√£ ƒë∆∞·ª£c s·ª≠ d·ª•ng!" : 
                       "S·ªë ƒëi·ªán tho·∫°i ƒë√£ ƒë∆∞·ª£c s·ª≠ d·ª•ng!"
            });
        }

        const hashedPassword = await bcrypt.hash(password, 10);
        const userId = await generateId("User");

        if (!userId) return res.status(500).json({ error: "Kh√¥ng th·ªÉ t·∫°o userId!" });

        const newUser = new User({ userId, username, phonenumber, email, password: hashedPassword });
        await newUser.save();

        res.status(201).json({ message: "ƒêƒÉng k√Ω th√†nh c√¥ng!", user: newUser });
    } catch (error) {
        console.error(error);
        res.status(500).json({ error: "L·ªói khi ƒëƒÉng k√Ω" });
    }
});


router.post("/login", async (req, res) => {
    try {
        let { username, email, password } = req.body;
        const query = email ? { email: email.toLowerCase().trim() } : { username: username.toLowerCase().trim() };
        const user = await User.findOne(query);

        if (!user) return res.status(401).json({ error: "Sai t√†i kho·∫£n ƒëƒÉng nh·∫≠p" });

        const isMatch = await bcrypt.compare(password, user.password);
        if (!isMatch) return res.status(401).json({ error: "Sai m·∫≠t kh·∫©u!" });

        const token = jwt.sign({ userId: user.userId }, process.env.JWT_SECRET, { expiresIn: "7d" });

        res.json({ message: "ƒêƒÉng nh·∫≠p th√†nh c√¥ng!", token, user });
    } catch (error) {
        console.error(error);
        res.status(500).json({ error: "L·ªói khi ƒëƒÉng nh·∫≠p" });
    }
});
