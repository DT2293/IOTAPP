router.post("/verify-otp", async (req, res) => {
    try {
        const { email, otp } = req.body;
        const stored = otpStore.get(email);

        if (!stored || stored.otp !== otp || Date.now() > stored.expiresAt) {
            return res.status(400).json({ error: "OTP kh√¥ng h·ª£p l·ªá ho·∫∑c ƒë√£ h·∫øt h·∫°n!" });
        }

        // ‚úÖ T√¨m l·∫°i user
        const user = await User.findOne({ email: email.toLowerCase().trim() });
        if (!user) return res.status(404).json({ error: "Ng∆∞·ªùi d√πng kh√¥ng t·ªìn t·∫°i!" });

        otpStore.delete(email); // X√≥a OTP sau khi d√πng

        // ‚úÖ T·∫°o tokenonPressed: otpController.text.trim().length == 6 ? _verifyOtp : null,

        const token = jwt.sign({ userId: user._id }, process.env.JWT_SECRET, { expiresIn: "1h" });
        res.json({ message: "X√°c th·ª±c th√†nh c√¥ng!", token, user });
    } catch (error) {
        console.error("L·ªói x√°c th·ª±c OTP:", error);
        res.status(500).json({ error: "L·ªói khi x√°c th·ª±c OTP" });
    }
});

