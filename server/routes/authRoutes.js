const otpStore = new Map(); // In-memory store: email -> { otp, expiresAt }

router.post("/forgot-password", async (req, res) => {
    try {
        const { email } = req.body;
        const user = await User.findOne({ email: email.toLowerCase().trim() });

        if (!user) return res.status(404).json({ error: "Email ch∆∞a ƒë∆∞·ª£c ƒëƒÉng k√Ω!" });

        // üîπ T·∫°o m√£ OTP 6 ch·ªØ s·ªë
        const otp = Math.floor(100000 + Math.random() * 900000).toString();
        const expiresAt = Date.now() + 60 * 1000; // OTP c√≥ hi·ªáu l·ª±c 1 ph√∫t

        otpStore.set(email, { otp, expiresAt });

        // üîπ C·∫•u h√¨nh email g·ª≠i ƒëi
        const mailOptions = {
            from: `"${process.env.SMTP_SENDER_NAME}" <${process.env.SMTP_SENDER_EMAIL}>`,
            to: user.email,
            subject: "M√£ OTP ƒëƒÉng nh·∫≠p",
            html: `
            <div style="font-family: Arial, sans-serif; padding: 20px; text-align: center">
                <h2>X√°c th·ª±c ƒëƒÉng nh·∫≠p</h2>
                <p>M√£ OTP c·ªßa b·∫°n l√†:</p>
                <div style="font-size: 24px; font-weight: bold; color: #2c3e50">${otp}</div>
                <p>M√£ c√≥ hi·ªáu l·ª±c trong v√≤ng 1 ph√∫t.</p>
            </div>
            `
        };

        await transporter.sendMail(mailOptions);

        res.json({ message: "M√£ OTP ƒë√£ ƒë∆∞·ª£c g·ª≠i ƒë·∫øn email c·ªßa b·∫°n!" });
    } catch (error) {
        console.error("L·ªói g·ª≠i OTP:", error);
        res.status(500).json({ error: "Kh√¥ng th·ªÉ g·ª≠i OTP" });
    }
});

